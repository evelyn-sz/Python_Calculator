def add(var_1, var_2):
    return var_1 + var_2
    

def subtract(var_1, var_2):
    return var_1 - var_2
    

def multiply(var_1, var_2):
    return var_1 * var_2
    

def divide(var_1, var_2):
    return var_1 / var_2
    

print(add(3,5))
print(subtract(5,3))
print(multiply(3,5))
print(divide(15,3))